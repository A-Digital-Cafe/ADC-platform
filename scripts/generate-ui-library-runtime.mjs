#!/usr/bin/env node
/* global console */
import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const projectRoot = process.cwd();
const appDir = path.join(projectRoot, "src/apps/public/00-adc-ui-library");
const configPath = path.join(appDir, "config.json");

const config = JSON.parse(await fs.readFile(configPath, "utf-8"));
const uiConfig = config.uiModule ?? {};
const moduleName = uiConfig.name ?? "adc-ui-library";
const namespace = uiConfig.uiNamespace ?? "default";
const outputDir = path.join(projectRoot, "temp/ui-builds", namespace, moduleName);

await fs.mkdir(outputDir, { recursive: true });

await fs.writeFile(path.join(outputDir, "init.js"), createInit(moduleName), "utf-8");
await fs.writeFile(path.join(outputDir, "styles.css"), await createStyles(moduleName), "utf-8");

console.log(`[ui-runtime] Generated ${path.relative(projectRoot, outputDir)}/init.js and styles.css`);

function createInit(name) {
	return `/**
 * Auto-init for ${name}.
 * Generated by scripts/generate-ui-library-runtime.mjs.
 */
import { defineCustomElements } from './loader/index.js';

if (typeof window !== 'undefined') {
	const key = Symbol.for('stencil-init:${name}');
	if (!globalThis[key]) {
		defineCustomElements(window);
		globalThis[key] = true;
	}
}

export * from './loader/index.js';
`;
}

async function createStyles(name) {
	const possibleCssPaths = [
		path.join(appDir, "src/global/tailwind.css"),
		path.join(appDir, "src/styles/tailwind.css"),
		path.join(appDir, "src/global/styles.css"),
		path.join(appDir, "src/global/accessibility.css"),
	];

	let combinedCss = "";

	for (const cssPath of possibleCssPaths) {
		try {
			const cssContent = await fs.readFile(cssPath, "utf-8");
			combinedCss += `\n/* ---- ${path.basename(cssPath)} ---- */\n`;
			combinedCss += extractPureCss(cssContent, name);
		} catch (error) {
			if (error?.code !== "ENOENT") throw error;
		}
	}

	return combinedCss.trim() ? combinedCss : `/* ${name} - No CSS source found */\n`;
}

function extractPureCss(cssContent, name) {
	let result = `/**\n * Base CSS for ${name}.\n * Generated automatically without Tailwind directives.\n */\n\n`;
	let cleaned = cssContent.replaceAll(/@import\s+["']tailwindcss["'];?\s*/g, "");

	const layerBaseMatch = /@layer\s+base\s*\{([\s\S]*?)\n\}/.exec(cleaned);
	if (layerBaseMatch) {
		result += `/* Base styles */\n${layerBaseMatch[1].trim()}\n\n`;
	}

	const layerComponentsMatch = /@layer\s+components\s*\{([\s\S]*?)\n\}/.exec(cleaned);
	if (layerComponentsMatch) {
		result += `/* Component styles */\n${layerComponentsMatch[1].trim()}\n\n`;
	}

	if (!layerBaseMatch && !layerComponentsMatch) {
		cleaned = cleaned.replaceAll(/@utility\s+[\w-]+\s*\{[^}]*\}/g, "");
		cleaned = cleaned.replaceAll(/@keyframes\s+[\w-]+\s*\{[\s\S]*?\}\s*\}/g, "");
		result = cleaned.trim() || result;
	}

	return result;
}